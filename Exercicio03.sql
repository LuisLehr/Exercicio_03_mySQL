-- RELATORIO GERAL DE TODOS OS CLIENTES
SELECT C.IDCLIENTE, C.NOME, C.SEXO, C.EMAIL, C.CPF, 
	   E.RUA, E.BAIRRO, E.CIDADE, E.ESTADO, 
	   T.TIPO, T.NUMERO
  FROM CLIENTE C
 INNER JOIN ENDERECO E
    ON C.IDCLIENTE = E.ID_CLIENTE
 INNER JOIN TELEFONE T
    ON C.IDCLIENTE = T.ID_CLIENTE;


-- RELATORIO DE HOMENS
SELECT C.IDCLIENTE, C.NOME, C.SEXO, C.EMAIL, C.CPF, 
	   E.RUA, E.BAIRRO, E.CIDADE, E.ESTADO, 
	   T.TIPO, T.NUMERO
  FROM CLIENTE C
 INNER JOIN ENDERECO E
    ON C.IDCLIENTE = E.ID_CLIENTE
 INNER JOIN TELEFONE T
    ON C.IDCLIENTE = T.ID_CLIENTE
 WHERE C.SEXO = 'M';


-- RELATORIO DE MULHERES
SELECT C.IDCLIENTE, C.NOME, C.SEXO, C.EMAIL, C.CPF, 
	   E.RUA, E.BAIRRO, E.CIDADE, E.ESTADO, 
	   T.TIPO, T.NUMERO
  FROM CLIENTE C
 INNER JOIN ENDERECO E
    ON C.IDCLIENTE = E.ID_CLIENTE
 INNER JOIN TELEFONE T
    ON C.IDCLIENTE = T.ID_CLIENTE
 WHERE C.SEXO = 'F'; 


-- QUANTIDADE DE HOMENS E MULHERES
SELECT SEXO, COUNT(*)
  FROM CLIENTE 
 GROUP BY SEXO;


-- IDS E EMAIL DAS MULHERES QUE MOREM NO CENTRO DO RIO DE JANEIRO E NAO TENHAM CELULAR
SELECT C.IDCLIENTE, C.NOME, C.EMAIL, E.BAIRRO, E.CIDADE, T.NUMERO
  FROM CLIENTE C
 INNER JOIN ENDERECO E
    ON C.IDCLIENTE = E.ID_CLIENTE
 INNER JOIN TELEFONE T
    ON C.IDCLIENTE = T.ID_CLIENTE
 WHERE E.BAIRRO LIKE '%CENTRO%'
   AND E.CIDADE = 'RIO DE JANEIRO'
   AND T.NUMERO = NULL;


-- MUDANDO OS CADASTROS COM O SEXO CADASTRADO ERRADO
UPDATE CLIENTE SET SEXO = 'F'
 WHERE IDCLIENTE IN (9, 10, 11, 15);


UPDATE CLIENTE SET SEXO = 'M'
 WHERE IDCLIENTE = '13'; 



